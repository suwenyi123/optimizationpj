    aj = R
    A=Trow(aj)
    x=rand([n,p])
    Y=eye(n)
    Y_0=Y
    z=g_v(c,b,x)
    grad_0=z
    x_record=zeros([max_k,n,p])
    for k = 1:max_k
        x=A*x-arf*z;
        Y=A*Y;
        grad_1=g_v(c,b,x)
        z=A*z+inv(diag(diag(Y)))*grad_1-inv(diag(diag(Y_0)))*grad_0;
        Y_0=Y
        grad_0=grad_1;
        x_record(k,:,:)=x
    end
end